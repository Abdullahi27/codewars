function areYouPlayingBanjo(name) {
    return name.charAt(0).toUpperCase() == 'R' ? name + " plays banjo"  : name + " does not play banjo" 
 
 }
 
 // If your name starts with the letter "R" or lower case "r", 
 // you are playing banjo!